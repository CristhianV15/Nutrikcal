    private void btnEvalNutriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEvalNutriActionPerformed
        //Validación
        if (txtCodigo.getText().equalsIgnoreCase("")) { //El codigo debe ser numerico, y esta oculto al usuario
            JOptionPane.showMessageDialog(null, "NO SE HA SELECCIONADO NINGUN REGISTRO");
        } else {
            // Variable del formulario de evaluación nutricional
            FormEvaluacion fEvaluacion = new FormEvaluacion();
            //Se muestra el formulario
            fEvaluacion.setVisible(true);
            //Se envia el nombre seleccionado al formEvaluación
            fEvaluacion.LbNombrePac.setText(txtNombre.getText());
            //Se envia el id seleccionado para las consultas
            fEvaluacion.txtCodigoPaciente.setText(txtCodigo.getText());
            //Enviar edad del paciente
            fEvaluacion.txtEdadP.setText(txtEdad.getText());
            //Enviar sexo del paciente
            if (txtSexo.getText().equalsIgnoreCase("M")) {
                fEvaluacion.txtSexoP.setText("Masculino");
            } else {
                fEvaluacion.txtSexoP.setText("Femenino");
            }
            
            //Enviar Correo del paciente
            fEvaluacion.lblCorreoUno.setText(txtCorreo.getText());
            
            
            dispose();
        }
    }//GEN-LAST:event_btnEvalNutriActionPerformed

