    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        //Validacion
        if (txtNombre.getText().isEmpty()|| txtApellido.getText().isEmpty() || txtTelefono.getText().isEmpty() || txtCorreo.getText().isEmpty()|| txtSexo.getText().isEmpty() || txtDNI.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "FALTAN INGRESAR DATOS ");
        }
        else{
        CPacientes objetoPaciente = new CPacientes();
        objetoPaciente.InsertarPacientes(txtNombre, txtApellido, txtTelefono, txtCorreo, txtSexo, txtDatosAdicionales, fechaNac, txtDNI);
        objetoPaciente.mostrarPacientes(tablePacientes);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

